    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        List menulist = TakeAwayRestaurantManagements.menulist;
        DefaultTableModel model = (DefaultTableModel) jMenutable.getModel();
        Object rowData[] = new Object[4];
        Iterator itr = menulist.iterator();
        Menulist ml;
        model.setNumRows(0);

        while (itr.hasNext()) {
            ml = (Menulist) itr.next();
            rowData[0] = ml.getMenu_id();
            rowData[1] = ml.getRes_id();
            rowData[2] = ml.getFood_name();
            rowData[3] = ml.getFood_price();

            model.addRow(rowData);
        }
        try {

            ResultSet res;
            String cell = jres_id.getText();
            String sql = "SELECT * FROM menulist WHERE res_id=" + cell;
            Statement stmt = connection.createStatement();
            res = stmt.executeQuery(sql);
            while (res.next()) {
                String menu_id = res.getString("menu_id");
                String res_id = res.getString("res_id");
                String food_name = res.getString("food_name");
                String food_price = res.getString("food_price");

                String tbData[] = {menu_id, res_id, food_name, food_price};
                DefaultTableModel tblModel = (DefaultTableModel) jMenutable.getModel();
                tblModel.addRow(tbData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Buzlubamde" + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

