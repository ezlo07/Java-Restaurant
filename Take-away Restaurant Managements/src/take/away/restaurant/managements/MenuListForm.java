    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jmenu_id.getText().trim().isEmpty()
                && !jres_id.getText().trim().isEmpty()
                && !jfood_name.getText().trim().isEmpty()
                && !jfood_price.getText().trim().isEmpty()) {
            int menu_id = Integer.parseInt(jmenu_id.getText().trim());
            int res_id = Integer.parseInt(jres_id.getText().trim());
            String food_name = jfood_name.getText().trim();
            float food_price = Float.parseFloat(jfood_price.getText().trim());

            TakeAwayRestaurantManagements.add_menulist(menu_id, res_id, food_name, food_price);
            JOptionPane.showMessageDialog(null, "Ok!");
            refresh_JTable();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

